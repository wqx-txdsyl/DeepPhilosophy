def split_concat(tag):
    """Split '存在主义女性主义' → ['存在主义', '女性主义']"""
    results = [tag]
    for kw in ['主义','学派','哲学','理论','思想','传统','运动']:
        idx = tag.find(kw)
        if idx > 0 and idx + len(kw) < len(tag):
            p1 = tag[:idx+len(kw)]
            p2 = tag[idx+len(kw):]
            if p1 not in results: results.append(p1)
            if p2 not in results: results.append(p2)
    return results

def fix_tags(tag_str):
    if not tag_str: return ''
    # Step 1: split by delimiters
    raw_tags = [t.strip() for t in re.split(r'[/,、，;；]\s*', tag_str) if t.strip()]
    # Step 2: split concatenated tags
    all_tags = []
    for t in raw_tags:
        all_tags.extend(split_concat(t))
    # Step 3: clean parentheses
    all_tags = [re.sub(r'[（(][^)）]*[)）]', '', t).strip() for t in all_tags]
    all_tags = [t for t in all_tags if t]
    # Step 4: merge and dedup
    merged, seen = [], set()
    for t in all_tags:
        c = SCHOOL_MERGE.get(t, t)
        if c not in seen:
            merged.append(c)
            seen.add(c)
    return ' / '.join(merged)

