# 3. APPLY TO ALL PHILOSOPHERS
# ============================================================
school_changes = 0
country_changes = 0
for n, v in data.items():
    old_s = v.get('school', '')
    new_s = fix_tags(old_s)
    if old_s != new_s:
        v['school'] = new_s
        school_changes += 1

    # Country: split, clean, normalize
    raw_c = v.get('country', '')
    if raw_c:
        parts = [p.strip() for p in re.split(r'[/,、，;；]\s*', raw_c) if p.strip()]
        # Clean parentheses
        parts = [re.sub(r'[（(][^)）]*[)）]', '', p).strip() for p in parts]
        # Normalize
        norm = []
        seen_c = set()
        for p in parts:
            if not p: continue
            normalized = COUNTRY_MERGE.get(p, p)
            if normalized not in seen_c:
                norm.append(normalized)
                seen_c.add(normalized)
        new_c = ' / '.join(norm)
        if new_c != raw_c:
            v['country'] = new_c
            country_changes += 1

print(f'School changes: {school_changes}')
print(f'Country changes: {country_changes}')

