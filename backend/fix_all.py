# === 1. Remove non-person entries ===
REMOVE_PATTERNS = ['之墨','之儒','之道','之法','之兵','先秦墨','后期墨','前期墨','先秦儒','前期法']
REMOVE_EXACT = []

to_remove = set()
for n in data:
    for p in REMOVE_PATTERNS:
        if p in n:
            to_remove.add(n)
            break
    if n in REMOVE_EXACT:
        to_remove.add(n)

for n in sorted(to_remove):
    print(f'Remove: {n}')
    data.pop(n, None)
print(f'After cleanup: {len(data)}')

