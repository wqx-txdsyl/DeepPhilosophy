def fix_tags(tag_str):
    if not tag_str: return ''
    tags = [t.strip() for t in re.split(r'[/,、，;；]', tag_str) if t.strip()]
    merged, seen = [], set()
    for tag in tags:
        c = MEGA_MERGE.get(tag, tag)
        if c not in seen:
            merged.append(c)
            seen.add(c)
    return ' / '.join(merged)

school_changes = 0
country_changes = 0
for n in data:
    old_s = data[n].get('school','')
    new_s = fix_tags(old_s)
    if old_s != new_s:
        data[n]['school'] = new_s
        school_changes += 1
    old_c = data[n].get('country','')
    new_c = fix_tags(old_c)
    if old_c != new_c:
        data[n]['country'] = new_c
        country_changes += 1

print(f'School tag changes: {school_changes}')
print(f'Country changes: {country_changes}')

