def fix(tag_str):
    if not tag_str: return ''
    tags = [t.strip() for t in re.split(r'[/,、，;；]\s*', tag_str) if t.strip()]
    merged, seen = [], set()
    for tag in tags:
        tag = re.sub(r'[（(][^)）]*[)）]', '', tag).strip()
        c = MERGE.get(tag, tag)
        if c not in seen: merged.append(c); seen.add(c)
    return ' / '.join(merged)

changes = 0
for n, v in data.items():
    old_s = v.get('school', '')
    new_s = fix(old_s)
    if old_s != new_s:
        v['school'] = new_s
        changes += 1
    old_c = v.get('country', '')
    new_c = fix(old_c)
    if old_c != new_c:
        v['country'] = new_c

with open('data/philosophers.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

tags = set()
for v in data.values():
    for t in v.get('school', '').split(' / '):
        if t.strip(): tags.add(t.strip())
print(f'School changes: {changes}')
print(f'Unique school tags: {len(tags)}')
for t in sorted(tags):
    count = sum(1 for v in data.values() if t in v.get('school', ''))
    print(f'  [{count}] {t}')
