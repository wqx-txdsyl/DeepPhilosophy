/** Normalize a tag to its canonical form */
export function normalizeTag(tag) {
  return TAG_NORM_MAP[tag] || tag;
}

/** Expand a tag for filter matching (splits composite + adds parent) */
export function expandTag(tag) {
  const result = [tag];
  for (const kw of ['主义', '哲学', '学派', '理论', '思想']) {
    const idx = tag.indexOf(kw);
    if (idx > 0 && idx + kw.length < tag.length) {
      const p1 = tag.slice(0, idx + kw.length);
      const p2 = tag.slice(idx + kw.length);
      if (p1 !== tag && !result.includes(p1)) result.push(p1);
      if (p2 !== tag && !result.includes(p2)) result.push(p2);
    }
  }
  const parent = normalizeTag(tag);
  if (parent !== tag && !result.includes(parent)) result.push(parent);
  const extras = TAG_EXPAND_MAP[tag] || [];
  for (const t of extras) {
    if (!result.includes(t)) result.push(t);
  }
  return result;
}

/** Normalize country name */
export function normalizeCountry(c) {
  return COUNTRY_NORM_MAP[c] || c;
}
