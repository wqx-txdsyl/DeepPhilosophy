def fix_tags(tag_str):
    if not tag_str: return ''
    # Use / only
    tags = [t.strip() for t in re.split(r'[/,、，;；]', tag_str) if t.strip()]
    merged, seen = [], set()
    for tag in tags:
        # Clean parentheses
        tag = re.sub(r'[（(][^)）]*[)）]', '', tag).strip()
        c = HARD_MERGE.get(tag, tag)
        if c not in seen:
            merged.append(c)
            seen.add(c)
    return ' / '.join(merged)

# Apply
school_changes = 0
for name, info in data.items():
    old = info.get('school','')
    new = fix_tags(old)
    if old != new:
        info['school'] = new
        school_changes += 1
    # Also clean country
    old_c = info.get('country','')
    new_c = fix_tags(old_c)
    if old_c != new_c:
        info['country'] = new_c

# Stats
unique = set()
for v in data.values():
    for t in v.get('school','').split(' / '):
        if t.strip(): unique.add(t.strip())

print(f'School changes: {school_changes}')
print(f'Unique tags: {len(unique)}')
for t in sorted(unique):
    c = sum(1 for v in data.values() if t in v.get('school',''))
    print(f'  [{c}] {t}')

with open(json_path, 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)
print(f'Done! {len(data)} philosophers, {len(unique)} tags')
