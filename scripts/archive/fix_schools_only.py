def fix_tags(tag_str):
    if not tag_str: return ''
    tags = [t.strip() for t in tag_str.replace('、','/').replace(',','/').replace('，','/').replace(';','/').replace('；','/').split('/') if t.strip()]
    merged, seen = [], set()
    for tag in tags:
        c = MERGE_MAP.get(tag, tag)
        if c not in seen:
            merged.append(c)
            seen.add(c)
    return ' / '.join(merged)

# === Modify PHILOSOPHERS in memory ===
school_changes = 0
country_changes = 0
for name, info in PHILOSOPHERS.items():
    old_s = info.get('school', '')
    new_s = fix_tags(old_s)
    if old_s != new_s:
        info['school'] = new_s
        school_changes += 1
    old_c = info.get('country', '')
    new_c = fix_tags(old_c)
    if old_c != new_c:
        info['country'] = new_c
        country_changes += 1

print(f'School changes: {school_changes}, Country changes: {country_changes}')

# === Write back: read file, replace each philosopher's school/country line ===
db_path = os.path.join(os.path.dirname(os.path.abspath(__file__)), 'philosophers_db.py')
with open(db_path, 'r', encoding='utf-8') as f:
    lines = f.readlines()

for name, info in PHILOSOPHERS.items():
    # Find the entry start
    for i, line in enumerate(lines):
        if line.strip().startswith(f'"{name}":'):
            # Search for school and country lines in the NEXT 200 lines (long bios)
            for j in range(i, min(i+200, len(lines))):
                if '"school":' in lines[j]:
                    indent = len(lines[j]) - len(lines[j].lstrip())
                    new_val = info['school'].replace('\\', '\\\\').replace('"', '\\"')
                    lines[j] = ' ' * indent + f'"school": "{new_val}",\n'
                elif '"country":' in lines[j]:
                    indent = len(lines[j]) - len(lines[j].lstrip())
                    new_val = info['country'].replace('\\', '\\\\').replace('"', '\\"')
                    lines[j] = ' ' * indent + f'"country": "{new_val}",\n'
                elif lines[j].strip().startswith('},'):
                    break
            break

with open(db_path, 'w', encoding='utf-8') as f:
    f.writelines(lines)

